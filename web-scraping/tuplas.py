tupla = (1,2,3,4,1,5,6,7,9,2,3,4)

#count cuenta las veces que se repite un elemento en la tupla
print(tupla)
print(tupla.count(1))

#len cuenta la cantidad de elementos de la tupla
# in pregunta si el elemento exite en la tupla

print(len(tupla))
print(5 in tupla)

#indexx busca la posicion de un elemento en la tupla
print(tupla.index(4))